public class EscapeSequences {

    public static void main(String[] args) {
        //Escape Sequences
        // \n - New Line
        // \t - Horizontal Tab
        // \r - Carriage Return
        // \b - Backspace
        // \f - Form Feed
        // \' - Single Quote
        // \" - Double Quote
        // \\ - Backslash

        System.out.println("Welcome \"Sd\"");
        System.out.println("Hi,\nHow are you?");

        System.out.println("C:\\xyz\\abc.txt");
    }

}
